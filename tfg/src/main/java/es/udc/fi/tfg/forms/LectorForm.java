package es.udc.fi.tfg.forms;

public class LectorForm {
	
	private String modelo;
	private String tipo;
	private String coord_x;
	private String coord_y;
	private String coord_z;
	private String edificio;
	private Long planta;
	private String area;
	private String zona;
	
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getCoord_x() {
		return coord_x;
	}
	public void setCoord_x(String coord_x) {
		this.coord_x = coord_x;
	}
	public String getCoord_y() {
		return coord_y;
	}
	public void setCoord_y(String coord_y) {
		this.coord_y = coord_y;
	}
	public String getCoord_z() {
		return coord_z;
	}
	public void setCoord_z(String coord_z) {
		this.coord_z = coord_z;
	}
	public String getEdificio() {
		return edificio;
	}
	public void setEdificio(String edificio) {
		this.edificio = edificio;
	}
	public Long getPlanta() {
		return planta;
	}
	public void setPlanta(Long planta) {
		this.planta = planta;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getZona() {
		return zona;
	}
	public void setZona(String zona) {
		this.zona = zona;
	}
	
	
	

}
